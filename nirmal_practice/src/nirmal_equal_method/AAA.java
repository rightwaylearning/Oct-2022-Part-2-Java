package nirmal_equal_method;

public class AAA {

	private int a;
	private int b;
	private String name;

	public AAA(int a, int b, String name) {

		this.a = a;
		this.b = b;
		this.name = name;

	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
